$(function(){
	$('.mini-cart').hover(function(){
      	$(this).closest('.header-top__cart')
      	.find('.cart-content')
      	.css('visibility','visible')
      	.show()
      	.addClass('is_cartHover');
    },function(){
      	$(this).closest('.header-top__cart')
      	.find('.cart-content')
      	.css('visibility','visible')
      	.hide()
      	.removeClass('is_cartHover');
    });

    $('.cart-content').hover(function(){
      	$(this).closest('.header-top__cart')
      	.find('.cart-content')
      	.css('visibility','visible')
      	.show()
      	.addClass('is_cartHover');
    },function(){
      	$(this).closest('.header-top__cart')
      	.find('.cart-content')
      	.css('visibility','visible')
      	.hide()
      	.removeClass('is_cartHover');
    });
})